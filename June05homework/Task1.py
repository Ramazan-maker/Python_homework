def print_smileys(n):
    if n == 0:  # базовый случай: если n равно 0, функция завершает свою работу
        return
    else:
        print("=)")  # выводим смайлик
        print_smileys(n - 1)  # рекурсивный вызов функции с уменьшенным значением n

# Пример использования функции:
print_smileys(5)  # Выводит 5 смайликов
